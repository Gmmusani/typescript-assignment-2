////   data types   ////

// There are seven data types:

// string,number,boolean,null,undefined,bigint,symbol.

// string :
let a : string = "Ghulam Mustafa";
console.log(a);

// number :
let b : number = 123;
console.log(b);

// boolean :
let isFollow : boolean = false
console.log(isFollow);

// null :
let empty : null = null
console.log(empty);

// undefined :
let e : undefined
console.log(e);

// bigint:
const bigIntValue : bigint = 1234567890123456789012345678901234567890n;
console.log(bigIntValue);

// symbol :
const mySym = Symbol("hello");
console.log(mySym);

////   CASE TYPE   ////

// there are three types of cases :

// camelCase :
let fullName : string = "Musani";
console.log(fullName);

// snake_case :
let full_name : string = "ausaf";
console.log(full_name);

// PascalCase :
let FullName : string = "hamza";
console.log(FullName);

////  ARRAY   ////

// Basic syntax ====> let variableName : data type[] = [value];

// in array index starts from zero.

let studentAge : number[] = [12 , 14 , 18];
console.log(studentAge);
console.log(studentAge[2]);

let colorName : string[] = ["white" ,"black" , "blue"];
colorName[2] = "orange"; 

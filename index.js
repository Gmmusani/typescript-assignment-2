////   variables   ////
// let
// const
// var
////   data types   ////
// There are seven data types:
// string,number,boolean,null,undefined,bigint,symbol.
// string :
let a = "Ghulam Mustafa";
console.log(a);
// number :
let b = 123;
console.log(b);
// boolean :
let isFollow = false;
console.log(isFollow);
// null :
let empty = null;
console.log(empty);
// undefined :
let e;
console.log(e);
// bigint:
const bigIntValue = 1234567890123456789012345678901234567890n;
console.log(bigIntValue);
// symbol :
const mySym = Symbol("hello");
console.log(mySym);
////   CASE TYPE   ////
// there are three types of cases :
// camelCase :
let fullName = "Musani";
console.log(fullName);
// snake_case :
let full_name = "ausaf";
console.log(full_name);
// PascalCase :
let FullName = "hamza";
console.log(FullName);
////  ARRAY   ////
// Basic syntax ====> let variableName : data type[] = [value];
// in array index starts from zero.
let studentAge = [12, 14, 18];
console.log(studentAge);
console.log(studentAge[2]);
let colorName = ["white", "black", "blue"];
colorName[2] = "orange";
console.log(colorName);
